    public UniversidadesForm(String Mode, double id) {
        initComponents();
        
         //setModal(true);
        Dimension dim = Toolkit.getDefaultToolkit().getScreenSize();
        this.setLocation(dim.width/2-this.getSize().width/2, dim.height/2-this.getSize().height/2);
        
        
        // INS, UPD, DEL, DSP
        _mode = Mode;
        if (!Mode.equals("INS")){
            //No es un insert por tando debe existir en la DB
            //Proceso para capturar el Registro
            System.out.println(id);
            currentUniversidad = UniversidadesModel.getUniversidadById(id);
            jTextField1.setText(String.valueOf(currentUniversidad.getId()));
            jTextField2.setText(currentUniversidad.getSiglas());
            jTextField3.setText(currentUniversidad.getNombrecomple());
            jTextField4.setText(currentUniversidad.getRector());
 
        
            
        } else {
            currentUniversidad= new Universidad();
        }
        
        switch(Mode){
            case "INS":
                
                JBConfirmar.setText("Agregar");
                break;
            case "UPD":
                
                JBConfirmar.setText("Editar");
                break;
            case "DEL":
                
                deshabilitarCampos();
                JBConfirmar.setText("Eliminar");
                break;
            case "DSP":
                JBConfirmar.setVisible(false);
                JBCancel.setText("Salir");
                deshabilitarCampos();
                
                break;
        }
        
    }
    
     private void deshabilitarCampos(){
         jTextField1.setEditable(false);
         jTextField2.setEditable(false);
         jTextField3.setEditable(false);
         jTextField4.setEditable(false);
   
    }

  

