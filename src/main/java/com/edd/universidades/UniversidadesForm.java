    private void JBConfirmarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JBConfirmarMouseClicked
        // TODO add your handling code here:
         //Extrer la informacion
        //currentUniversidad.setId(valueOf(jTextField1.getText()));       
        currentUniversidad.setSiglas(jTextField2.getText());
        currentUniversidad.setNombrecomple(jTextField3.getText());
        currentUniversidad.setRector(jTextField4.getText());
        //Validaciones Expresiones Regulares
        
        switch(_mode){
            case "INS":
                _reloadParent = UniversidadesModel.agregarUniversidad(currentUniversidad);
                break;
            case "UPD":
                _reloadParent = UniversidadesModel.editarUniversidad(currentUniversidad);
                break;
            case "DEL":
                _reloadParent = UniversidadesModel.eliminarUniversidad(currentUniversidad);
                break;
        }
        
        dispatchEvent(new WindowEvent(this, WindowEvent.WINDOW_CLOSING));
    }//GEN-LAST:event_JBConfirmarMouseClicked

